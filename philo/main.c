#include "philo.h"

/* ************************************************************************** */
/*                                                                            */
/* Helper prototypes                                                          */
/*                                                                            */
/* ************************************************************************** */

noreturn void	help(void);

/* ************************************************************************** */
/*                                                                            */
/* Main                                                                       */
/*                                                                            */
/* ************************************************************************** */

int	main(
		int argc,
		char **argv
		)
{
	t_philo_ctx	ctx;

	if (argc < PROG__MIN || argc > PROG__MAX)
		help();
	philo_init_args(&ctx, argc, argv);
	philo_init(&ctx);
	ft_time_ms();
	philo_start(&ctx);
	philo_destroy(&ctx);
	return (EXIT_SUCCESS);
}

/* ************************************************************************** */
/*                                                                            */
/* Helper implementation                                                      */
/*                                                                            */
/* ************************************************************************** */

noreturn
void	help(void)
{
	printf("Usage: philo " ARG1 " " ARG2 " " ARG3 " " ARG4 " [" ARG5 "]\n");
	exit(EXIT_SUCCESS);
}
